'use client';

import { useState } from 'react';
import { InputHTMLAttributes, LabelHTMLAttributes, ChangeEvent, KeyboardEvent } from 'react';

/**
 * Props for FormInput component
 */
interface FormInputProps extends InputHTMLAttributes<HTMLInputElement> {
  label: string;
  error?: string;
  labelProps?: LabelHTMLAttributes<HTMLLabelElement>;
}

/**
 * Reusable form input component with label and error handling
 * 
 * Features:
 * - Consistent styling across the application
 * - Error state visualization
 * - Accessible label association
 * - Focus states and transitions
 * 
 * @example
 * ```tsx
 * <FormInput
 *   label="Amount"
 *   type="number"
 *   value={amount}
 *   onChange={(e) => setAmount(e.target.value)}
 *   error={errors.amount}
 * />
 * ```
 */
export default function FormInput({
  label,
  error,
  id,
  labelProps,
  className = '',
  onChange,
  type,
  maxLength,
  ...inputProps
}: FormInputProps) {
  // Generate ID if not provided (for label association)
  const inputId = id || `input-${label.toLowerCase().replace(/\s+/g, '-')}`;
  
  // State for password visibility toggle
  const [showPassword, setShowPassword] = useState(false);
  const isPasswordField = type === 'password';

  /**
   * Prevents non-numeric keys from being pressed in numeric fields
   * Allows: digits (0-9), decimal point (.), backspace, delete, arrow keys, etc.
   */
  const handleKeyDown = (e: KeyboardEvent<HTMLInputElement>) => {
    if (type === 'number') {
      const key = e.key;
      
      // Allow control keys (backspace, delete, tab, escape, enter, etc.)
      const controlKeys = [
        'Backspace', 'Delete', 'Tab', 'Escape', 'Enter',
        'ArrowLeft', 'ArrowRight', 'ArrowUp', 'ArrowDown',
        'Home', 'End', 'Clear'
      ];
      
      // Allow Ctrl/Cmd + A, C, V, X, Z (copy, paste, cut, undo, select all)
      if (e.ctrlKey || e.metaKey) {
        return;
      }
      
      // Block if it's not a control key and not a valid numeric character
      if (!controlKeys.includes(key) && !/^[0-9.]$/.test(key)) {
        e.preventDefault();
        return;
      }
      
      // Prevent multiple decimal points
      if (key === '.' && (e.currentTarget.value || '').includes('.')) {
        e.preventDefault();
        return;
      }
    }
  };

  /**
   * Handles input changes with validation for numeric fields
   * - Prevents non-numeric characters for number inputs
   * - Enforces maxLength for number inputs
   */
  const handleChange = (e: ChangeEvent<HTMLInputElement>) => {
    if (type === 'number') {
      let value = e.target.value;
      
      // Remove any non-numeric characters (only allow digits and one decimal point)
      let filteredValue = value.replace(/[^0-9.]/g, '');
      
      // Ensure only one decimal point
      const parts = filteredValue.split('.');
      if (parts.length > 2) {
        filteredValue = parts[0] + '.' + parts.slice(1).join('');
      }
      
      // Enforce maxLength (default 6 for numeric fields)
      const maxLen = maxLength || 6;
      if (filteredValue.length > maxLen) {
        filteredValue = filteredValue.slice(0, maxLen);
      }
      
      // Update the input value directly
      e.target.value = filteredValue;
      
      // Create new event with filtered value
      const syntheticEvent = {
        ...e,
        target: {
          ...e.target,
          value: filteredValue,
        },
      } as ChangeEvent<HTMLInputElement>;
      
      // Call original onChange if provided
      if (onChange) {
        onChange(syntheticEvent);
      }
    } else {
      // For non-numeric fields, call onChange directly
      if (onChange) {
        onChange(e);
      }
    }
  };

  // Set default maxLength for number inputs if not provided
  const effectiveMaxLength = type === 'number' ? (maxLength || 6) : maxLength;
  
  // Determine input type based on password visibility
  const inputType = isPasswordField 
    ? (showPassword ? 'text' : 'password')
    : (type === 'number' ? 'text' : type);

  return (
    <div className="space-y-1">
      <label
        htmlFor={inputId}
        className="block text-sm font-medium text-gray-700 mb-1"
        {...labelProps}
      >
        {label}
      </label>
      <div className="relative">
        <input
          id={inputId}
          type={inputType}
          inputMode={type === 'number' ? 'decimal' : undefined}
          maxLength={effectiveMaxLength}
          className={`w-full px-4 py-2.5 border rounded-lg transition-colors
            focus:ring-2 focus:ring-blue-500 focus:border-transparent
            disabled:bg-gray-100 disabled:cursor-not-allowed
            ${error ? 'border-red-500 focus:ring-red-500' : 'border-gray-300'}
            ${type === 'number' ? '[appearance:textfield] [&::-webkit-outer-spin-button]:appearance-none [&::-webkit-inner-spin-button]:appearance-none' : ''}
            ${isPasswordField ? 'pr-10' : ''}
            ${className}`}
          style={type === 'number' ? { MozAppearance: 'textfield' } : undefined}
          aria-invalid={error ? 'true' : 'false'}
          aria-describedby={error ? `${inputId}-error` : undefined}
          onKeyDown={handleKeyDown}
          onChange={handleChange}
          {...inputProps}
        />
        {isPasswordField && (
          <button
            type="button"
            onClick={() => setShowPassword(!showPassword)}
            className="absolute right-3 top-1/2 -translate-y-1/2 text-gray-500 hover:text-gray-700 focus:outline-none focus:text-gray-700 transition-colors cursor-pointer"
            aria-label={showPassword ? 'Hide password' : 'Show password'}
            tabIndex={0}
          >
            {showPassword ? (
              // Eye open icon (visible password)
              <svg
                xmlns="http://www.w3.org/2000/svg"
                className="h-5 w-5"
                fill="none"
                viewBox="0 0 24 24"
                stroke="currentColor"
                strokeWidth={2}
              >
                <path
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  d="M15 12a3 3 0 11-6 0 3 3 0 016 0z"
                />
                <path
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  d="M2.458 12C3.732 7.943 7.523 5 12 5c4.478 0 8.268 2.943 9.542 7-1.274 4.057-5.064 7-9.542 7-4.477 0-8.268-2.943-9.542-7z"
                />
              </svg>
            ) : (
              // Eye closed icon (hidden password)
              <svg
                xmlns="http://www.w3.org/2000/svg"
                className="h-5 w-5"
                fill="none"
                viewBox="0 0 24 24"
                stroke="currentColor"
                strokeWidth={2}
              >
                <path
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  d="M13.875 18.825A10.05 10.05 0 0112 19c-4.478 0-8.268-2.943-9.543-7a9.97 9.97 0 011.563-3.029m5.858.908a3 3 0 114.243 4.243M9.878 9.878l4.242 4.242M9.88 9.88l-3.29-3.29m7.532 7.532l3.29 3.29M3 3l3.59 3.59m0 0A9.953 9.953 0 0112 5c4.478 0 8.268 2.943 9.543 7a10.025 10.025 0 01-4.132 5.411m0 0L21 21"
                />
              </svg>
            )}
          </button>
        )}
      </div>
      {error && (
        <p
          id={`${inputId}-error`}
          className="mt-1 text-sm text-red-600"
          role="alert"
        >
          {error}
        </p>
      )}
    </div>
  );
}

